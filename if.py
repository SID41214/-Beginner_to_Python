# if Statement for X
X = 10
if X < 34:
    print("x is less than 34")
print("***************************************")
# End of if block

# if Statement for Y
Y = 6
if Y > 34:
    print("y is greater than 34")
print("statement outside the if block")
print("***************************************")
# End of if block

# if Statement for Z
Z = 2
if Z < 5:
    print("x is less than 5")
print("***************************************")
# End of if block

# Short-hand if-else
age = 20
print("you can't vote") if age < 18 else print("you can vote")
print("Done")
